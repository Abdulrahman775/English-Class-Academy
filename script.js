const html = document.documentElement;
const menuToggle = document.querySelector(".menu-toggle");
const navPanel = document.querySelector(".nav-panel");
const languageToggle = document.querySelector("[data-language-toggle]");
const supportedLanguages = ["ar", "en"];

let currentLanguage = getSavedLanguage();

function getSavedLanguage() {
  try {
    const saved = localStorage.getItem("eca-language");
    return supportedLanguages.includes(saved) ? saved : "ar";
  } catch {
    return "ar";
  }
}

function saveLanguage(language) {
  try {
    localStorage.setItem("eca-language", language);
  } catch {
    return null;
  }
  return language;
}

function translatePage(language) {
  const dictionary = translations[language] || translations.ar;
  currentLanguage = language;
  html.lang = language;
  html.dir = language === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (Object.prototype.hasOwnProperty.call(dictionary, key)) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const pairs = element.dataset.i18nAttr.split(",");
    pairs.forEach((pair) => {
      const [attribute, key] = pair.split(":").map((part) => part.trim());
      if (attribute && key && Object.prototype.hasOwnProperty.call(dictionary, key)) {
        element.setAttribute(attribute, dictionary[key]);
      }
    });
  });

  updateMenuLabel();
  updateWhatsAppLinks();
}

function createWhatsAppLink(message) {
  return `https://wa.me/${WHATSAPP_NUMBER}?text=${encodeURIComponent(message)}`;
}

function updateWhatsAppLinks() {
  const messages = whatsappMessages[currentLanguage] || whatsappMessages.ar;
  document.querySelectorAll("[data-whatsapp]").forEach((link) => {
    const messageKey = link.dataset.whatsapp || "general";
    const message = messages[messageKey] || messages.general;
    link.href = createWhatsAppLink(message);
    link.target = "_blank";
    link.rel = "noopener noreferrer";
  });
}

function updateMenuLabel() {
  if (!menuToggle) return;
  const dictionary = translations[currentLanguage] || translations.ar;
  const isOpen = menuToggle.getAttribute("aria-expanded") === "true";
  menuToggle.setAttribute("aria-label", isOpen ? dictionary.menuClose : dictionary.menuOpen);
}

function setMenuState(isOpen) {
  if (!menuToggle || !navPanel) return;
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  navPanel.classList.toggle("is-open", isOpen);
  updateMenuLabel();
}

if (menuToggle && navPanel) {
  menuToggle.addEventListener("click", () => {
    const isOpen = menuToggle.getAttribute("aria-expanded") !== "true";
    setMenuState(isOpen);
  });

  navPanel.addEventListener("click", (event) => {
    if (event.target.closest("a")) {
      setMenuState(false);
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      setMenuState(false);
    }
  });
}

if (languageToggle) {
  languageToggle.addEventListener("click", () => {
    const nextLanguage = currentLanguage === "ar" ? "en" : "ar";
    saveLanguage(nextLanguage);
    translatePage(nextLanguage);
    setMenuState(false);
  });
}

document.querySelectorAll(".faq-list summary").forEach((summary) => {
  summary.tabIndex = 0;
  let ignoreNextClick = false;
  const toggleDetails = () => {
    const details = summary.parentElement;
    if (details instanceof HTMLDetailsElement) {
      details.open = !details.open;
    }
  };

  summary.addEventListener("click", (event) => {
    event.preventDefault();
    if (ignoreNextClick) {
      ignoreNextClick = false;
      return;
    }
    toggleDetails();
  });

  summary.addEventListener("keydown", (event) => {
    if (event.key !== "Enter" && event.key !== " ") return;
    event.preventDefault();
    ignoreNextClick = true;
    toggleDetails();
  });
});

function revealOnScroll() {
  const elements = document.querySelectorAll("[data-reveal]");
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

  if (!("IntersectionObserver" in window) || reduceMotion) {
    elements.forEach((element) => element.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.14 }
  );

  elements.forEach((element) => observer.observe(element));
}

translatePage(currentLanguage);
